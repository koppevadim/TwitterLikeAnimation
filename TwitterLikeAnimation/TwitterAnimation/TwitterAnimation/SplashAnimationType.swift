import Foundation

/**
 The types of animation supported
 
 - Twitter: The default animation type is the Twitter App animation
 */
public enum SplashAnimationType: String{
    
    case twitter
    case rotateOut
    case woobleAndZoomOut
    case swingAndZoomOut
    case popAndZoomOut
    case squeezeAndZoomOut
    case heartBeat
    
}
